# Run training with `--configs A,B,C`.  Algorithm selection is purely from the
# chained configs; the unified entry point is `treetune.main` (GEAR is
# auto-registered when `treetune` is imported).
#
# Usage: gear_run "<exp_name>" "configs/foo.jsonnet,configs/bar.jsonnet" [extra args]
gear_run() {
  local exp_name="$1"; shift
  local cfgs="$1"; shift
  local no_sample_replacement_config gpu_config runtime_config resolved_cfgs
  no_sample_replacement_config="${GEAR_ROOT}/configs/episode_generators/noSamplRplc.jsonnet"
  gpu_config="$(ensure_gpu_config "${GEAR_FIRST_GPU}")"
  runtime_config="$(ensure_runtime_config)"
  resolved_cfgs="${cfgs}"
  if [[ ",${resolved_cfgs}," != *",${no_sample_replacement_config},"* ]]; then
    resolved_cfgs+=",${no_sample_replacement_config}"
  fi
  resolved_cfgs+=",${gpu_config}"
  if [[ -n "${runtime_config}" ]]; then
    resolved_cfgs+=",${runtime_config}"
  fi
  WANDB_PROJECT="${WANDB_PROJECT}" \
  APP_EXPERIMENT_NAME="${exp_name}" \
  deepspeed --master_port "${MASTER_PORT}" --include "localhost:${GEAR_GPU_INCLUDE}" \
    "${GEAR_ROOT}/treetune/main.py" \
    --configs "${resolved_cfgs}" \
    "$@" \
    run_iteration_loop
}

gear_eval() {
  local exp_name="$1"; shift
  local cfgs="$1"; shift
  local last_policy="$1"; shift
  local gpu_config
  gpu_config="$(ensure_gpu_config "${GEAR_FIRST_GPU}")"
  WANDB_PROJECT="${WANDB_PROJECT}" \
  APP_EXPERIMENT_NAME="${exp_name}" \
  deepspeed --master_port "${MASTER_PORT}" --include "localhost:${GEAR_GPU_INCLUDE}" \
    "${GEAR_ROOT}/treetune/main.py" \
    --configs "${cfgs},${gpu_config}" \
    "$@" \
    evaluate --iteration 0 --last_policy_path "${last_policy}"
}

# ensure_tree_config <shape>
# Resolve a tree shape (e.g. "666", "6666", "3456", "8888") to a jsonnet
# overlay path.  If a checked-in config exists unde
#   configs/episode_generators/branch_factor_<shape>.jsonnet
# we use it as-is.  Otherwise we auto-generate one unde
#   configs/episode_generators/_generated/branch_factor_<shape>.jsonnet
# so any shape works without hand-editing the repo.
#
# Shape is a string of digits 1-9, one digit per depth level (the i-th
# digit is the branch factor at depth i).  Generated configs use an M
# (per-segment token cap) default by depth: <=3 -> 600, 4 -> 500, 5 -> 400,
# >=6 -> 300.  TREE_M=<positive-int> is appended as a final overlay so it also
# overrides checked-in and previously generated configs.
#
# Prints one config path, or a comma-separated config list when TREE_M is set.
ensure_tree_config() {
  local shape="$1"
  local checked_in="${GEAR_ROOT}/configs/episode_generators/branch_factor_${shape}.jsonnet"
  local tree_config

  if [[ -f "${checked_in}" ]]; then
    tree_config="${checked_in}"
  else
    if ! [[ "${shape}" =~ ^[1-9]+$ ]]; then
      echo "[tree] invalid tree shape '${shape}': must be a string of digits 1-9 (e.g. 666, 3456)" >&2
      return 1
    fi

    local gen_dir="${GEAR_ROOT}/configs/episode_generators/_generated"
    local out="${gen_dir}/branch_factor_${shape}.jsonnet"
    local depth=${#shape}
    local M
    if   [[ ${depth} -le 3 ]]; then M=600
    elif [[ ${depth} -eq 4 ]]; then M=500
    elif [[ ${depth} -eq 5 ]]; then M=400
    else                            M=300
    fi

    if [[ ! -f "${out}" ]]; then
      mkdir -p "${gen_dir}"
      {
        echo "// Auto-generated by scripts/_common.sh:ensure_tree_config."
        echo "// Shape='${shape}' depth=${depth} M=${M}."
        echo "{"
        echo "  episode_generator+: {"
        echo "    inference_strategy+: {"
        echo "      M: ${M},"
        echo "      max_depth: ${depth},"
        echo "      branch_factor_strategy+: {"
        echo "        branch_factors: ["
        local i
        for (( i=0; i<depth; i++ )); do
          echo "          { depth: ${i}, branch_factor: ${shape:$i:1} },"
        done
        echo "        ],"
        echo "      },"
        echo "    },"
        echo "  },"
        echo "}"
      } > "${out}"

      echo "[tree] generated branch_factor_${shape}.jsonnet (depth=${depth}, M=${M})" >&2
    fi
    tree_config="${out}"
  fi

  if [[ -z "${TREE_M:-}" ]]; then
    echo "${tree_config}"
    return 0
  fi

  if ! [[ "${TREE_M}" =~ ^[1-9][0-9]*$ ]]; then
    echo "[tree] invalid TREE_M='${TREE_M}': must be a positive integer" >&2
    return 1
  fi

  local m_gen_dir="${APP_DIRECTORY}/_generated_configs/tree_overrides"
  local m_override="${m_gen_dir}/tree_M_${TREE_M}.jsonnet"
  mkdir -p "${m_gen_dir}"
  cat > "${m_override}" <<EOF
// Auto-generated by scripts/_common.sh:ensure_tree_config.
{
  episode_generator+: {
    inference_strategy+: {
      M: ${TREE_M},
    },
  },
}
EOF

  echo "${tree_config},${m_override}"
}

default_model_base_for_suffix() {
  local suffix="$1"
  case "${suffix}" in
    gear_tree|spo_tree|spo_chain|grpo)
      echo "qwen1_5b_base"
      ;;
    ppo|dpo_positive|restem)
      echo "rho1bSft2"
      ;;
    *)
      echo "qwen1_5b_base"
      ;;
  esac
}

# resolve_math_config <suffix> <model_alias>
# First prefer a fully materialized config:
#   configs/polIter_<model_alias>_<suffix>_MATH.jsonnet
# If it does not exist but configs/model_overrides/<model_alias>.jsonnet does,
# compose a known base config with that model override. This keeps new model
# support compact while preserving existing hand-tuned configs.
resolve_math_config() {
  local suffix="$1"
  local model="$2"
  local direct="${GEAR_ROOT}/configs/polIter_${model}_${suffix}_MATH.jsonnet"
  if [[ -f "${direct}" ]]; then
    echo "${direct}"
    return 0
  fi

  local override="${GEAR_ROOT}/configs/model_overrides/${model}.jsonnet"
  if [[ -f "${override}" ]]; then
    local base="${MODEL_BASE:-$(default_model_base_for_suffix "${suffix}")}"
    local base_cfg="${GEAR_ROOT}/configs/polIter_${base}_${suffix}_MATH.jsonnet"
    if [[ ! -f "${base_cfg}" ]]; then
      echo "[model] missing base config for MODEL=${model}: ${base_cfg}" >&2
      return 1
    fi
    echo "${base_cfg},${override}"
    return 0
  fi

  echo "[model] unknown MODEL=${model}; expected ${direct} or ${override}" >&2
  return 1
}

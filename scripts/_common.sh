#!/usr/bin/env bash
# Shared environment setup for all training scripts in this repo.
# Source this file at the top of every entry-point script.

set -euo pipefail

# Locate project paths.
GEAR_ROOT="${GEAR_ROOT:-$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)}"

# PYTHONPATH: project root so `treetune` and `guidance` packages resolve.
export PYTHONPATH="${GEAR_ROOT}:${PYTHONPATH:-}"

# Jsonnet `import` resolves under the unified configs/ tree.
export APP_JSONNET_PATH="${GEAR_ROOT}/configs:${GEAR_ROOT}"

# Standard env vars (kept compatible with upstream SPO conventions).
export APP_SEED="${APP_SEED:-42}"
export APP_DISABLE_FLASH_ATTENTION="${APP_DISABLE_FLASH_ATTENTION:-${DISABLE_FLASH_ATTENTION:-0}}"
export MASTER_PORT="${MASTER_PORT:-$(python3 -c "import socket; s=socket.socket(socket.AF_INET, socket.SOCK_STREAM); s.bind(('', 0)); print(s.getsockname()[1]); s.close()")}"
export APP_DIRECTORY="${APP_DIRECTORY:-${GEAR_ROOT}/experiments}"
export APP_MINIMIZE_STORED_FILES="${APP_MINIMIZE_STORED_FILES:-True}"
export WANDB_PROJECT="${WANDB_PROJECT:-treetune}"
mkdir -p "${APP_DIRECTORY}"

# Default GPU is index 0; override with GEAR_GPU=N or GEAR_GPUS=0,1,2,3.
# With GEAR_GPUS, Deepspeed launches one process per listed GPU.  Episode
# generation is sharded by process, so each rank can host its own vLLM server.
GEAR_GPU_INCLUDE="${GEAR_GPUS:-${GEAR_GPU:-0}}"
GEAR_FIRST_GPU="${GEAR_GPU_INCLUDE%%,*}"

# Helpers ---------------------------------------------------------------------
jsonnet_value() {
  local value="$1"
  case "${value}" in
    auto)
      echo "'auto'"
      ;;
    true|false|null)
      echo "${value}"
      ;;
    *)
      echo "${value}"
      ;;
  esac
}

ensure_gpu_config() {
  local first_gpu="$1"
  local eval_gpu="${GEAR_EVALUATE_GPU:-${first_gpu}}"
  local vllm_gpu_offset="${GEAR_VLLM_GPU_OFFSET:-${first_gpu}}"
  local gen_dir="${APP_DIRECTORY}/_generated_configs/gpus"
  local out="${gen_dir}/gpu_${first_gpu}_eval_${eval_gpu}_vllm_offset_${vllm_gpu_offset}.jsonnet"

  mkdir -p "${gen_dir}"
  cat > "${out}" <<EOF
// Auto-generated by scripts/_common.sh:ensure_gpu_config.
// Deepspeed include: localhost:${GEAR_GPU_INCLUDE}
// Set GEAR_VLLM_GPU_OFFSET to put generation vLLM servers on another GPU block.
{
  evaluate_gpu: ${eval_gpu},
  episode_generator+: {
    gpu_offset: ${vllm_gpu_offset},
  },
}
EOF
  echo "${out}"
}

ensure_runtime_config() {
  if [[ "${GEAR_FAST_OVERLAY:-1}" == "0" ]]; then
    return 0
  fi

  local gen_dir="${APP_DIRECTORY}/_generated_configs/runtime_overrides"
  local out="${gen_dir}/short_run_fast_runtime.jsonnet"

  local eval_every="${GEAR_EVAL_EVERY_N_ITERATIONS:-10}"
  local save_steps="${GEAR_SAVE_STEPS:-10}"
  local keep_steps="${GEAR_CHECKPOINT_KEEP_STEPS:-1}"
  local kl_coef="${GEAR_KL_COEF:-0.0}"
  local target_train_batch_size="${GEAR_TARGET_TRAIN_BATCH_SIZE:-128}"
  local gradient_accumulation_steps="${GEAR_GRADIENT_ACCUMULATION_STEPS:-null}"
  local per_device_train_batch_size="${GEAR_PER_DEVICE_TRAIN_BATCH_SIZE:-2}"
  if [[ "${gradient_accumulation_steps}" != "null" && -z "${GEAR_PER_DEVICE_TRAIN_BATCH_SIZE:-}" ]]; then
    per_device_train_batch_size="null"
  fi
  local per_device_eval_batch_size="${GEAR_PER_DEVICE_EVAL_BATCH_SIZE:-2}"
  local num_epochs_per_iteration="${GEAR_NUM_EPOCHS_PER_ITERATION:-1}"
  local num_episodes_per_iteration="${GEAR_NUM_EPISODES_PER_ITERATION:-null}"
  local dataset_num_samples_per_iteration="${GEAR_DATASET_NUM_SAMPLES_PER_ITERATION:-null}"
  local total_num_iterations="${GEAR_TOTAL_NUM_ITERATIONS:-null}"
  local vllm_gpu_mem
  vllm_gpu_mem="$(jsonnet_value "${GEAR_VLLM_GPU_MEMORY_UTILIZATION:-auto}")"
  local vllm_min_free="${GEAR_VLLM_MIN_AVAILABLE_GPU_MEMORY_MB:-1024}"
  local max_programs="${GEAR_MAX_CONCURRENT_PROGRAMS:-256}"
  local max_generations="${GEAR_MAX_CONCURRENT_GENERATIONS:-256}"
  local value_max_programs="${GEAR_VALUE_MAX_CONCURRENT_PROGRAMS:-512}"
  local value_max_generations="${GEAR_VALUE_MAX_CONCURRENT_GENERATIONS:-512}"
  local vllm_max_num_seqs="${GEAR_VLLM_MAX_NUM_SEQS:-1024}"
  local vllm_swap_space="${GEAR_VLLM_SWAP_SPACE:-null}"
  local gear_inference_overrides=""
  local gear_episode_overrides=""
  if [[ -n "${GEAR_SCORE_CONCURRENCY:-}" ]]; then
    gear_inference_overrides+="      gear_score_concurrency: ${GEAR_SCORE_CONCURRENCY},"$'\n'
  fi
  if [[ -n "${EPS_TAIL_CALIBRATION_PATH:-}" ]]; then
